package com.capgemini.CoreJava.basics;

public class Variable {
	 

	public Variable() {
		System.out.println("It is variable class");

	}

	public static void main(String[] args) {
		byte b = 10;         //declaring and initialization byte
		short s = 101;       //declaring and initialization short
		int i = 101;         //declaring and initialization integer
		long l = 10L;        //declaring and initialization long
		float f = 101.01F;   //declaring and initialization float
		double d = 123.456;  //declaring and initialization double
		char c='S';          //declaring and initialization char
		boolean q = true;    //declaring and initialization boolean
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(q);


		
	}
}
