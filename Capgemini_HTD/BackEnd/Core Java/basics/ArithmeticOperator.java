package com.capgemini.CoreJava.basics;

public class ArithmeticOperator {
	public static void main(String[] args) {
		int i=10;
		int j=20;
		
		int res=i+j;					//addition
		System.out.println("i+j="+res);
		
		res=i-j;						//subtraction
		System.out.println("i-j="+res);
		
		res=i*j;						//multiplication
		System.out.println("i*j="+res);
		
		res=j/i;						//division
		System.out.println("i/j="+res);
		
		res=i%j;						//modulus
		System.out.println("i%j="+res);
	}

}
